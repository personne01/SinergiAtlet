export function getSport(id: string): SportDef | undefined {
  return SPORTS.find((s) => s.id === id);
}

export function getSportLevel(sportId: string, levelId: string): SportLevel | undefined {
  const sport = getSport(sportId);
  if (!sport) return undefined;
  return sport.levels.find((l) => l.id === levelId);
}

export function getDimensionItems(sportId: string, dimensionId: string): SkillDimensionDef | undefined {
  const sport = getSport(sportId);
  if (!sport) return undefined;
  for (const level of sport.levels) {
    const dim = level.dimensions.find((d) => d.id === dimensionId);
    if (dim) return dim;
  }
  return undefined;
}
